#include "../minishell.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char	*ptr;

	if (!src)
		return (NULL);
	ptr = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (ptr);
}
